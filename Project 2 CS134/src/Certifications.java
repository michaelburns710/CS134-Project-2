
public class Certifications {

		private String name;
		private Double cost;
		private Double hoursToComplete;
		private Double compensationFactor;
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public Double getCost() {
			return cost;
		}
		public void setCost(Double cost) {
			this.cost = cost;
		}
		public Double getHoursToComplete() {
			return hoursToComplete;
		}
		public void setHoursToComplete(Double hoursToComplete) {
			this.hoursToComplete = hoursToComplete;
		}
		public Double getCompensationFactor() {
			return compensationFactor;
		}
		public void setCompensationFactor(Double compensationFactor) {
			this.compensationFactor = compensationFactor;
		}
		
		
}
